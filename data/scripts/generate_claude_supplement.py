# ── write to file ──────────────────────────────────────────────────────────────

def main() -> None:
    train_path = Path("data/processed/mental_dialogue_train.jsonl")
    with open(train_path, "a", encoding="utf-8") as f:
        for record in RECORDS:
            f.write(json.dumps(record, ensure_ascii=False) + "\n")

    total = sum(1 for _ in open(train_path, encoding="utf-8"))
    print(f"Appended {len(RECORDS)} records.  New train size: {total}")

    # quick distribution check
    import collections
    emotion_counts: dict = collections.Counter()
    intent_counts: dict  = collections.Counter()
    all_records = []
    with open(train_path, encoding="utf-8") as f:
        for line in f:
            all_records.append(json.loads(line))
    for r2 in all_records:
        emotion_counts[r2["emotion_label"]] += 1
        intent_counts[r2["intent_label"]]  += 1
    n = len(all_records)
    print("\nEmotion distribution after update:")
    for lbl, cnt in sorted(emotion_counts.items(), key=lambda x: -x[1]):
        print("  %-20s %5d  (%.1f%%)" % (lbl, cnt, cnt/n*100))
    print("\nIntent distribution after update:")
    for lbl, cnt in sorted(intent_counts.items(), key=lambda x: -x[1]):
        print("  %-25s %5d  (%.1f%%)" % (lbl, cnt, cnt/n*100))


if __name__ == "__main__":
    main()
