EMOTION_LABELS = ["anxiety", "sadness", "anger", "fear", "shame", "hopelessness", "neutral"]
INTENT_LABELS  = ["venting", "seeking_advice", "seeking_empathy", "crisis",
                  "self_disclosure", "information_seeking"]


def r(primary: str, labels: list[str], secondary: dict[str, float]) -> dict[str, float]:
    """Build a score dict: primary gets 1-sum(secondary), rest 0."""
    scores = {k: 0.0 for k in labels}
    scores.update(secondary)
    scores[primary] = round(1.0 - sum(secondary.values()), 4)
    # fix any float drift
    total = sum(scores.values())
    if abs(total - 1.0) > 1e-9:
        scores[primary] = round(scores[primary] + (1.0 - total), 4)
    return scores


def rec(text: str, el: str, escores: dict, il: str, iscores: dict,
        intensity: float, risk: bool = False) -> dict:
    return {
        "id": str(uuid.uuid4()),
        "text": text,
        "emotion_label": el,
        "emotion_scores": escores,
        "intent_label": il,
        "intent_scores": iscores,
        "intensity_score": round(intensity, 4),
        "risk_flag": risk,
        "source": "synthetic_claude",
    }


# ── helpers: common score profiles ────────────────────────────────────────────

def ne(sec=None):  # neutral emotion
    return r("neutral", EMOTION_LABELS, sec or {"anxiety": 0.1, "sadness": 0.05})

def fe(sec=None):  # fear emotion
    return r("fear", EMOTION_LABELS, sec or {"anxiety": 0.2, "sadness": 0.05})

def ae(sec=None):  # anger emotion
    return r("anger", EMOTION_LABELS, sec or {"sadness": 0.1, "shame": 0.05})

def he(sec=None):  # hopelessness emotion
    return r("hopelessness", EMOTION_LABELS, sec or {"sadness": 0.15, "anxiety": 0.05})

def vi(sec=None):   return r("venting",             INTENT_LABELS, sec or {"seeking_empathy": 0.15})
def sa(sec=None):   return r("seeking_advice",      INTENT_LABELS, sec or {"venting": 0.1})
def se(sec=None):   return r("seeking_empathy",     INTENT_LABELS, sec or {"venting": 0.15})
def sd(sec=None):   return r("self_disclosure",     INTENT_LABELS, sec or {"venting": 0.1})
def inf(sec=None):  return r("information_seeking", INTENT_LABELS, sec or {"seeking_advice": 0.1})


