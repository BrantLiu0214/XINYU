def main() -> None:
    path = Path("data/processed/mental_dialogue_test.jsonl")
    # back up human-annotated version
    human_path = Path("data/processed/mental_dialogue_test.human.jsonl")
    if path.exists() and not human_path.exists():
        shutil.copy(path, human_path)
        print(f"Human-annotated test saved to {human_path}")

    with open(path, "w", encoding="utf-8") as f:
        for r in R:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")
    print(f"Wrote {len(R)} balanced records to {path}")

    # distribution report
    import collections
    ec: dict = collections.Counter(r["emotion_label"] for r in R)
    ic: dict = collections.Counter(r["intent_label"] for r in R)
    risk_n = sum(1 for r in R if r["risk_flag"])
    n = len(R)
    print("\nEmotion distribution:")
    for lbl,cnt in sorted(ec.items(), key=lambda x:-x[1]):
        print("  %-20s %4d  (%.1f%%)" % (lbl, cnt, cnt/n*100))
    print("\nIntent distribution:")
    for lbl,cnt in sorted(ic.items(), key=lambda x:-x[1]):
        print("  %-25s %4d  (%.1f%%)" % (lbl, cnt, cnt/n*100))
    print("\nrisk_flag=True: %d (%.1f%%)" % (risk_n, risk_n/n*100))


if __name__ == "__main__":
    main()
