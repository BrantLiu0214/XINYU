"""Generate a balanced test set: 70 records per emotion class × 7 = 490 total.
Replaces mental_dialogue_test.jsonl; human-annotated version saved as .human.jsonl.

Run from project root:
    python data/scripts/generate_balanced_test.py
"""
from __future__ import annotations
import json, shutil, uuid
from pathlib import Path

EMOTION_LABELS = ["anxiety","sadness","anger","fear","shame","hopelessness","neutral"]
INTENT_LABELS  = ["venting","seeking_advice","seeking_empathy","crisis",
                  "self_disclosure","information_seeking"]

def sc(primary, labels, secondary):
    d = {k: 0.0 for k in labels}
    d.update(secondary)
    d[primary] = round(1.0 - sum(secondary.values()), 4)
    t = sum(d.values())
    if abs(t-1.0) > 1e-9:
        d[primary] = round(d[primary]+(1.0-t), 4)
    return d

def mk(text, el, il, intensity, risk=False,
       e_sec=None, i_sec=None):
    return {"id": str(uuid.uuid4()), "text": text,
            "emotion_label": el,
            "emotion_scores": sc(el, EMOTION_LABELS, e_sec or {}),
            "intent_label": il,
            "intent_scores": sc(il, INTENT_LABELS, i_sec or {}),
            "intensity_score": round(intensity, 4),
            "risk_flag": risk, "source": "synthetic_claude_balanced_test"}

